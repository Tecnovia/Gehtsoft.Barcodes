namespace Gehtsoft.Barcodes.Enums
{
    /// <summary>
    /// Specifies a set of supported barcode rotation angles
    /// </summary>
    public enum BarcodeRotation
    {
        /// <summary>
        /// Rotate 0 degrees (no rotation)
        /// </summary>
        Clockwise_0,

        /// <summary>
        /// Rotate 90 degrees clockwise
        /// </summary>
        Clockwise_90,

        /// <summary>
        /// Rotate 180 degrees clockwise
        /// </summary>
        Clockwise_180,

        /// <summary>
        /// Rotate 270 degrees clockwise
        /// </summary>
        Clockwise_270
    }
}
