namespace Gehtsoft.Barcodes.Enums
{
    /// <summary>
    /// Specifies a set of supported barcode rotation angles.
    /// </summary>
    public enum BarcodeRotation
    {
        /// <summary>
        /// 0 degree (no rotation).
        /// </summary>
        Clockwise_0,

        /// <summary>
        /// 90 degree clockwise rotation.
        /// </summary>
        Clockwise_90,

        /// <summary>
        /// 180 degree clockwise rotation.
        /// </summary>
        Clockwise_180,

        /// <summary>
        /// 270 degree clockwise rotation.
        /// </summary>
        Clockwise_270
    }
}
